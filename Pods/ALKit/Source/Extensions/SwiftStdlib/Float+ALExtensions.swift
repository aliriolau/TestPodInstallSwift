import Foundation
import CoreGraphics

extension Float: ALCompatible { }
public extension ALWrapper where Base == Float {

  var int: Int {
    return Int(base)
  }

  var double: Double {
    return Double(base)
  }

  var cgFloat: CGFloat {
    return CGFloat(base)
  }

}
